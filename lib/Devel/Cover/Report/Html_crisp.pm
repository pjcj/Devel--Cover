  if ($R{common_prefix}) {
    $o .= <<HTML;
<div class="common-prefix">
<span class="common-prefix-label">Prefix:</span>
<span class="common-prefix-path">$R{common_prefix}</span>
</div>
HTML
  }

  $o .= render_worst_files(\@worst);
  $o .= render_dist_bar($dist);

  $o .= <<HTML;
<div class="filter-bar">
<input type="text" class="filter-input"
  placeholder="Filter files (regex)..." aria-label="Filter files">
<label class="filter-check">
<input type="checkbox" class="hide-covered"> Hide 100% covered
</label>
<label class="filter-check">
<input type="checkbox" class="group-toggle"> Group by directory
</label>
</div>
HTML

  my $ncrit  = $R{criteria}->@*;
  my $ncols  = $ncrit + 2;       # criteria + total + slop
  my $crit_w = 70 / $ncols;
  $o
    .= qq(<table class="file-table">\n<colgroup>\n)
    . qq(<col style="width:30%">\n)
    . (qq(<col style="width:${crit_w}%">\n) x $ncols)
    . "</colgroup>\n<thead>\n<tr>\n"
    . qq(<th data-sort="file">File</th>\n);

  for my $c ($R{criteria}->@*) {
    $o .= qq(<th data-sort="$c">@{[ crit_name($c) ]}</th>\n);
  }
  $o .= <<HTML;
<th data-sort="total">@{[ crit_name('total') ]}</th>
<th data-sort="slop">SLOP</th>
</tr>
</thead>
<tbody>
HTML

  for my $g (@groups) {
    $o .= qq(<tr class="dir-header" data-dir="$g->{dir}">\n)
      . "<td>$g->{dir}</td>\n";
    for my $c ($R{criteria}->@*) {
      $o .= cov_cell($g->{criteria}{$c}, 0);
    }
    $o .= cov_cell($g->{total}, 0);
    $o .= <<HTML;
