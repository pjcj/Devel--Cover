sub file_nav ($prev_file, $next_file) {
  my $prev
    = $prev_file
    ? qq(<a href="$prev_file->{link}" class="nav-prev">)
    . "&laquo; $prev_file->{short}</a>"
    : "";
  my $next
    = $next_file
    ? qq(<a href="$next_file->{link}" class="nav-next">)
    . "$next_file->{short} &raquo;</a>"
    : "";
  <<HTML
<div class="file-nav">
<span>$prev</span>
<span><a href="$R{options}{option}{outputfile}">&uarr; Summary</a></span>
<span>$next</span>
</div>
HTML
}

sub class ($pc, $err, $criterion) {
  return "" if $criterion && $criterion eq "time";
  return "na" unless defined $pc && $pc =~ /\A[0-9.]+\z/;
  no warnings "uninitialized";
     !$err                   ? "c3"
    : $pc < $Threshold->{c0} ? "c0"
    : $pc < $Threshold->{c1} ? "c1"
    : $pc < $Threshold->{c2} ? "c2"
    :                          "c3"
}

sub oclass ($o, $criterion) {
  $o ? class($o->percentage, $o->error, $criterion) : ""
}

sub fmt_pc ($pc) {
  defined $pc
    ? (sprintf "%5.2f", $pc) =~ s/.\z//r =~ s/^\s+//r =~ s/^100\.0$/100/r
    : "n/a"
}

sub _format_criterion ($part, $criterion) {
  return { pc => "n/a", class => "na", covered => 0, total => 0, error => 0 }
    unless $part && exists $part->{$criterion};
  my $c = $part->{$criterion};
  {
    pc      => fmt_pc($c->{percentage}),
    class   => class($c->{percentage}, $c->{error}, $criterion),
    covered => $c->{covered} || 0,
    total   => $c->{total}   || 0,
    error   => $c->{error}   || 0,
  }
}

sub get_summary ($file, $criterion) {
  _format_criterion($R{db}->summary($file), $criterion)
}

sub _apply_slop ($f, $slop_data) {
  if ($slop_data && defined $slop_data->{file_crap}) {
    $f->{file_crap} = sprintf "%.1f", $slop_data->{file_crap};
    $f->{file_slop} = sprintf "%.1f", $slop_data->{file_slop} // 0;
    $f->{file_cc}   = $slop_data->{file_cc};
    $f->{file_cov}  = sprintf "%.0f", $slop_data->{file_cov};
    my @sorted = sort { $b->{crap} <=> $a->{crap} } grep defined $_->{crap},
      @{ $slop_data->{subs} || [] };
    $f->{worst_subs} = [
      map { {
        name => $_->{name},
        crap => sprintf("%.1f", $_->{crap}),
        slop => $_->{slop},
      } } @sorted[0 .. ($#sorted > 2 ? 2 : $#sorted)]
    ];
  } else {
    $f->{file_crap}  = 0;
    $f->{file_slop}  = 0;
    $f->{file_cc}    = 0;
    $f->{file_cov}   = 0;
    $f->{worst_subs} = [];
  }
}

sub build_one_file ($file) {
  return unless $R{db}->summary($file);
  my $dir = $file =~ s|/[^/]+$||r;
  $dir = "" if $dir eq $file;
  my $basename   = $file =~ s|.*/||r;
  my $meta       = $R{db}->cover->file($file)->{meta} // {};
  my $uncompiled = $meta->{uncompiled} ? 1 : 0;
  my %f          = (
    name       => $file,
    basename   => $basename,
    dir        => $dir,
    link       => "$R{filenames}{$file}.html",
    exists     => -e $file,
    uncompiled => $uncompiled,
    criteria   => {},
  );

  for my $c ($R{showing}->@*) {
    my $s = get_summary($file, $c);
    $s->{class}      = "c0"  if $uncompiled && $s->{pc} eq "n/a";
    $s->{pc}         = "0.0" if $uncompiled && $s->{pc} eq "n/a";
    $f{criteria}{$c} = $s;
  }
  my $total = get_summary($file, "total");
  if ($uncompiled && $total->{pc} eq "n/a") {
    $total->{class} = "c0";
    $total->{pc}    = "0.0";
  }
  $f{total} = $total;
  my $pc = $total->{pc} // "n/a";
  $f{total_pc}   = $pc;
  $f{total_sort} = $pc eq "n/a" ? -1 : $pc;
  _apply_slop(\%f, $R{db}->summary($file, "slop"));
  \%f
}

sub build_file_data () {
  my @file_data;
  for my $file ($R{options}{file}->@*) {
    my $f = build_one_file($file);
    push @file_data, $f if $f;
  } sort {
         ($b->{file_slop} || 0)   <=> ($a->{file_slop} || 0)
      || ($a->{total_sort} // -1) <=> ($b->{total_sort} // -1)
      || $a->{name} cmp $b->{name}
  } @file_data
}

sub get_dir_summary ($dir, $criterion) {
  _format_criterion($R{db}->dir_summary($dir), $criterion)
}

sub build_dir_groups ($file_data) {
  my %dirs;
  push $dirs{ $_->{dir} }->@*, $_ for @$file_data;
  map {
    my $dir = $_;
    my $g   = {
      dir      => $dir || "(root)",
      dir_key  => $dir,
      total    => get_dir_summary($dir, "total"),
      criteria =>
        { map { $_ => get_dir_summary($dir, $_) } $R{criteria}->@* },
      files => $dirs{$dir},
    };
    $g->{pc}    = $g->{total}{pc};
    $g->{class} = $g->{total}{class};
    _apply_slop($g, $R{db}->dir_summary($dir, "slop"));
    $g;
  } sort keys %dirs
}

sub line_subroutines ($f, $n) {
  my $subs = $f->subroutine or return;
  my $loc  = $subs->location($n);
  return unless $loc && @$loc;
  my $cl = $R{slop_subs} || {};
  map { {
    name    => encode_entities($_->name),
    covered => $_->covered,
    class   => oclass($_, "subroutine"),
    cc      => ($cl->{ "$n\0" . $_->name } // {})->{cc},
    crap    => ($cl->{ "$n\0" . $_->name } // {})->{crap},
    slop    => ($cl->{ "$n\0" . $_->name } // {})->{slop},
  } } @$loc
}

sub line_pod ($f, $n) {
  my $pod = $f->pod or return;
  my $loc = $pod->location($n);
  return unless $loc && @$loc;
  map { { covered => $_->covered, class => oclass($_, "pod") } } @$loc
}

sub line_branches ($f, $n) {
  my $branches = $f->branch or return;
  my $loc      = $branches->location($n);
  return unless $loc && @$loc;
  map {
    my $t = $_->value(0);
    my $f = $_->value(1);
    {
      true_count  => $t,
      false_count => $f,
      total_count => $t + $f,
      true_class  => class($t, $_->error(0), "branch"),
      false_class => class($f, $_->error(1), "branch"),
      text        => encode_entities($_->text // ""),
    }
  } @$loc
}

sub line_truth_tables ($f, $n) {
  my $conditions = $f->condition or return;
  my $loc        = $conditions->location($n);
  return unless $loc && @$loc;
  grep { $_->{rows}->@* } map {
    my @rows = map { {
      inputs  => $_->inputs,
      result  => $_->result,
      covered => $_->covered,
      class   => $_->covered ? "c3" : "c0",
    } } $_->rows;
    my @labels  = $_->labels;
    my @headers = map { chr ord("A") + $_ } 0 .. $#labels;
    {
      expr       => encode_entities($_->expr),
      short_expr => encode_entities($_->short_expr),
      rows       => \@rows,
      headers    => \@headers,
      legend     => [
        map { {
          letter => $headers[$_], label => encode_entities($labels[$_]) } }
          0 .. $#labels
      ],
    }
  } Devel::Cover::Condition_table->for_line($loc)
}

sub exec_class ($count) {
  defined $count && $count > 0 ? "exec-covered" : "exec-0"
}

sub line_statement ($f, $n, $line) {
  my $stmt = $f->statement or return;
  my $loc  = $stmt->location($n);
  return unless $loc && @$loc;
  my $s = $loc->[0];
  $line->{count}       = $s->covered;
  $line->{count_class} = oclass($s, "statement");
  $line->{exec_class}  = exec_class($s->covered);
}

sub line_partial ($line, $bd, $tts, $sd) {
  return unless defined $line->{count} && $line->{count} > 0;
  my $p;
  $p ||= any { $_->{true_count} == 0 || $_->{false_count} == 0 } @$bd;
  $p ||= any { any { !$_->{covered} } $_->{rows}->@* } @$tts;
  $p ||= any { !$_->{covered} } @$sd;
  $p ||= $line->{pod_uncovered};
  $line->{partial} = 1 if $p;
}

sub read_source ($file) {
  open my $fh, "<", $file or warn("Unable to open $file: $!\n"), return;
  my @all_lines = <$fh>;
  close $fh or die "Can't close $file: $!\n";

  if ($Have_highlighter) {
    my @hl = highlight($R{options}{option}, @all_lines);
    @all_lines = @hl if @hl;
  }
  @all_lines
}

sub build_source_lines ($file) {
  my $f = $R{db}->cover->file($file);

  my @all_lines = read_source($file) or return [];

  my @lines;
  my $n = 0;
  for my $l (@all_lines) {
    $n++;
    chomp $l;

    my %line = (number => $n, text => length $l ? $l : "&nbsp;");

    line_statement($f, $n, \%line);

    my @bd = line_branches($f, $n);
    $line{branches} = \@bd if @bd;

    my @tts = line_truth_tables($f, $n);
    $line{truth_tables} = \@tts if @tts;

    my @sd = line_subroutines($f, $n);
    $line{subroutines} = \@sd if @sd;

    my @pd = line_pod($f, $n);
    if (@pd) {
      $line{pod}           = \@pd;
      $line{pod_uncovered} = 1 if any { !$_->{covered} } @pd;
    }

    line_partial(\%line, \@bd, \@tts, \@sd);

    my @errors;
    push @errors, "branch"
      if any { $_->{true_count} == 0 || $_->{false_count} == 0 } @bd;
    push @errors, "condition" if any {
      any { !$_->{covered} }
        $_->{rows}->@*
    } @tts;
    push @errors, "subroutine" if any { !$_->{covered} } @sd;
    push @errors, "pod"        if $line{pod_uncovered};
    $line{errors} = join ",", @errors if @errors;

    push @lines, \%line;
    last if $l =~ /^__(END|DATA)__/;
  }

  \@lines
}

sub build_untested_source_lines ($file) {
  my @all_lines = read_source($file) or return [];

  my @lines;
  my $n = 0;
  for my $l (@all_lines) {
    $n++;
    chomp $l;
    my %line = (number => $n, text => length $l ? $l : "&nbsp;");
    push @lines, \%line;
    last if $l =~ /^__(END|DATA)__/;
  }

  \@lines
}

sub write_file ($path, $content) {
  open my $fh, ">", $path or die "Can't open $path: $!\n";
  print $fh $content;
  close $fh or die "Can't close $path: $!\n";
}

sub get_options ($self, $opt) {
  $opt->{option}{outputfile} = "coverage.html";
  $opt->{option}{restrict}   = 1;
  $Threshold->{$_}           = $opt->{"report_$_"}
    for grep { defined $opt->{"report_$_"} } qw( c0 c1 c2 );
  die "Invalid command line options"
    unless GetOptions($opt->{option}, qw( noppihtml noperltidy outputfile=s ));
}

sub favicon ($level) {
  state $map = { c0 => "none", c1 => "low", c2 => "good", c3 => "full" };
  $Cov->{light}{ $map->{$level} // "full" }{border} =~ s/^#/%23/r
}

sub set_favicon_colour () {
  my $t  = get_summary("Total", "total");
  my $pc = $t->{pc};
  $pc = 0 if !defined $pc || $pc eq "n/a";
  my $cl = class($pc, $pc < 100 ? 1 : 0, "total");
  $R{favicon_colour} = favicon($cl);
}

sub totals_for ($file) {
  my %total;
  for my $c ($R{showing}->@*) {
    $total{$c} = get_summary($file, $c);
  }
  $total{total} = get_summary($file, "total");
  %total
}

sub coverage_distribution ($file_data) {
  my %dist = (c0 => 0, c1 => 0, c2 => 0, c3 => 0, untested => 0);
  for my $fd (@$file_data) {
    if ($fd->{uncompiled}) {
      $dist{untested}++;
    } else {
      my $cl = $fd->{total}{class} || "c3";
      $dist{$cl}++ if exists $dist{$cl};
    }
  }
  $dist{dist_total} = @$file_data || 1;

  my $pl = sub ($n) { $n == 1 ? "file" : "files" };
  my $t  = $Threshold;
  $dist{tip_c0} = "$dist{c0} ${\$pl->($dist{c0})}" . " &lt; $t->{c0}%";
  $dist{tip_c1} = "$dist{c1} ${\$pl->($dist{c1})}" . " $t->{c0}-$t->{c1}%";
  $dist{tip_c2} = "$dist{c2} ${\$pl->($dist{c2})}" . " $t->{c1}-100%";
  $dist{tip_c3} = "$dist{c3} ${\$pl->($dist{c3})}" . " 100%";
  $dist{tip_untested} = "$dist{untested} untested ${\$pl->($dist{untested})}";
  %dist
}

sub generate_index ($outdir, $options, $file_data, $total, $dist) {
  my $html = "$outdir/$options->{option}{outputfile}";
  write_file($html, render_index($file_data, $total, $dist));
}

sub generate_file_pages ($outdir, $file_data) {
  for my $idx (0 .. $#$file_data) {
    my $fd = $file_data->[$idx];
    next unless $fd->{exists};
    my $file = $fd->{name};
    $R{slop_subs} = $R{db}->slop_sub_lookup($file);
    my $lines
      = $fd->{uncompiled}
      ? build_untested_source_lines($file)
      : build_source_lines($file);
    my %file_total = $fd->{uncompiled}
      ? (
        map { $_ => $fd->{criteria}{$_} // $fd->{total} } $R{showing}->@*,
        "total",
      )
      : totals_for($file);

    my $prev = $idx > 0            ? $file_data->[$idx - 1] : undef;
    my $next = $idx < $#$file_data ? $file_data->[$idx + 1] : undef;

    write_file(
      "$outdir/$fd->{link}",
      render_file_page($fd, $lines, \%file_total, $prev, $next),
    );
    delete $R{slop_subs};
  }
}

sub report ($pkg, $db, $options) {
  my $fname = (sort keys $db->{runs}->%*)[0] or return;
  my $run   = $db->{runs}{$fname};

  %R = (
    module   => { name => $run->name, version => $run->version },
    db       => $db,
    date     => strftime("%Y-%m-%d %H:%M:%S %z", localtime),
    perl_v   => $^V,
    os       => $^O,
    options  => $options,
    version  => $VERSION,
    showing  => [grep $options->{show}{$_}, $db->criteria],
    criteria => [grep $_ ne "time", grep $options->{show}{$_}, $db->criteria],
    headers  => [
      map  { ($db->criteria_short)[$_] }
      grep { $options->{show}{ ($db->criteria)[$_] } }
        (0 .. $db->criteria - 1)
    ],
    short => do {
      my @c = $db->criteria;
      my @s = $db->criteria_short;
      +{ (map { $c[$_] => $s[$_] } 0 .. $#c), total => "total" }
    },
    full => do {
      my @c = $db->criteria;
      +{ (map { $_ => ucfirst } @c), total => "total" }
    },
    filenames => {
      map { $_ => do { (my $f = $_) =~ s/\W/-/g; $f } } $options->{file}->@*
    },
    threshold      => $Threshold,
    have_ppi       => eval { require PPI; 1 } ? 1 : 0,
    favicon_colour => favicon("c3"),
    report_id      => $options->{outputdir},
    file_count     => 0 + $options->{file}->@*,
  );

  set_favicon_colour;

  my $outdir = $options->{outputdir};
  my $assets = "$outdir/assets";
  mkpath($assets) unless -d $assets;

  write_file("$assets/style.css", $Assets{css});
  write_file("$assets/app.js",    $Assets{js});

  my @file_data = build_file_data;

  my ($prefix, $short_map) = common_prefix(map { $_->{name} } @file_data);
  $R{common_prefix} = $prefix;
  if ($prefix) {
    for my $f (@file_data) {
      my $s = $short_map->{ $f->{name} };
      $f->{short} = $s;
      (my $dir = $s) =~ s{/[^/]+$}{};
      $dir           = "" if $dir eq $s;
      $f->{dir}      = $dir;
      $f->{basename} = $s =~ s{.*/}{}r;
    }
  } else {
    $_->{short} = $_->{name} for @file_data;
  }

  my %total = totals_for("Total");
  my %dist  = coverage_distribution(\@file_data);

  generate_index($outdir, $options, \@file_data, \%total, \%dist);
  generate_file_pages($outdir, \@file_data);

  my $html = "$outdir/$options->{option}{outputfile}";
  print "HTML output written to $html\n" unless $options->{silent};
}

