sub slop_class ($slop) {
  return "" unless defined $slop;
  $slop < 16 ? "c3" : $slop < 34 ? "c2" : $slop < 41 ? "c1" : "c0"
}

sub render_line_detail ($line) {
  my $o = qq(<tr class="line-detail"><td colspan="4">\n);

  if ($line->{subroutines}) {
    for my $s ($line->{subroutines}->@*) {
      my $status  = $s->{covered} ? "called" : "not called";
      my $cc_info = "";
      if (defined $s->{cc}) {
        my $cls  = slop_class($s->{slop});
        my $slop = sprintf "%.1f", $s->{slop} // 0;
        $cc_info = qq( <span class="$cls">CC=$s->{cc} SLOP=$slop</span>);
      }
      $o .= <<HTML;
<div class="detail">
<span class="detail-heading">Subroutine</span>
<span class="$s->{class}">$s->{name}: $status$cc_info</span>
</div>
HTML
    }
  }

  if ($line->{pod}) {
    for my $p ($line->{pod}->@*) {
      my $status = $p->{covered} ? "documented" : "undocumented";
      $o .= <<HTML;
<div class="detail">
<span class="detail-heading">Pod</span>
<span class="$p->{class}">$status</span>
</div>
HTML
    }
  }

  if ($line->{branches}) {
    my $nb = $line->{branches}->@*;
    my $pl = $nb > 1 ? "es" : "";
    $o .= <<HTML;
<div class="detail">
<span class="detail-heading">Branch: $nb branch$pl</span>
<table>
<tr><th>Branch</th><th>True</th><th>False</th><th>Total</th></tr>
HTML
    for my $b ($line->{branches}->@*) {
      $o .= <<HTML;
<tr><td>$b->{text}</td>
<td class="$b->{true_class}">$b->{true_count}</td>
<td class="$b->{false_class}">$b->{false_count}</td>
<td>$b->{total_count}</td></tr>
HTML
    }
    $o .= "</table>\n</div>\n";
  }

  if ($line->{truth_tables}) {
    for my $tt ($line->{truth_tables}->@*) {
      $o .= <<HTML;
<div class="detail">
<span class="detail-heading">Condition: $tt->{short_expr}</span>
HTML
      if ($tt->{legend} && $tt->{legend}->@*) {
        $o .= qq(<div class="tt-legend">\n);
        for my $item ($tt->{legend}->@*) {
          $o .= '<div class="tt-legend-item">'
            . qq(<strong>$item->{letter}</strong> = $item->{label}</div>\n);
        }
        $o .= "</div>\n";
      }
      $o .= "<table>\n<tr>\n";
      $o .= "<th>$_</th>" for $tt->{headers}->@*;
      $o .= "<th>result</th>\n</tr>\n";
      for my $row ($tt->{rows}->@*) {
        $o .= qq(<tr class="$row->{class}">);
        $o .= "<td>$_</td>" for $row->{inputs}->@*;
        $o .= "<td>$row->{result}</td></tr>\n";
      }
      $o .= "</table>\n</div>\n";
    }
  }

  $o .= "</td></tr>\n";
  $o
}

sub count_cell ($line, $cov_defined) {
  my $cls = "count";
  if ($line->{partial}) {
    $cls .= " exec-partial";
  } else {
    $cls .= " $line->{exec_class}" if $line->{exec_class};
    $cls .= " c0"                  if $line->{pod_uncovered};
  }
  my $aria
    = $cov_defined ? "executed $line->{count} times" : "no coverage data";
  my $count = $cov_defined ? $line->{count} : "";
  <<HTML
<td role="cell" class="$cls" aria-label="$aria">$count</td>
HTML
}

sub render_source_line ($line) {
  my $cov_defined = defined $line->{count};
  my $is_uncov
    = $cov_defined
    && $line->{count} == 0
    && ($line->{count_class} // "") eq "c0";
  my $has_detail
    = $line->{branches}
    || $line->{truth_tables}
    || $line->{subroutines}
    || $line->{pod};

  my $tr_cls = "";
  $tr_cls .= "src-c0"      if $is_uncov;
  $tr_cls .= " has-detail" if $has_detail;
  $tr_cls =~ s/\A //;

  my $o = '<tr role="row"';
  if ($cov_defined) {
    my $dcov = $line->{count} == 0 ? "0" : $line->{partial} ? "2" : "1";
    $o .= qq(\n    data-cov="$dcov");
  }
  $o .= qq(\n    class="$tr_cls")          if $tr_cls;
  $o .= qq( data-errors="$line->{errors}") if $line->{errors};
  $o .= ">\n";

  my $n = $line->{number};
  $o .= qq(<td role="cell" class="ln"><a id="L$n" href="#L$n">$n</a></td>\n);

  $o .= count_cell($line, $cov_defined);

  my $chevron = $has_detail ? "&#x25b6;" : "";
  my $src_cls = "src";
  $src_cls .= " src-c0"      if $is_uncov;
  $src_cls .= " src-partial" if $line->{partial};
  $src_cls .= " src-c1"      if ($line->{count_class} // "") eq "c1";
  my $text  = $line->{text} // "";
  $o .= qq(<td role="cell" class="chevron">$chevron</td>)
    . qq(<td role="cell" class="$src_cls">$text</td></tr>\n);

  $o .= render_line_detail($line) if $has_detail;
  $o
}

